public class project3 {
    public static void main(String[] args) {
        System.out.println("Operator Aritmatika");
        int a, b;
        double c;
        
        a = 10;
        b = 5;
        c = 2.45;

        System.out.println("a + b = " + (a + b));
        System.out.println("a - b = " + (a - b));
        System.out.println("a * b = " + (a * b));
        System.out.println("a / b = " + (a / b));
        System.out.println("a % b = " + (a % b));
        System.out.println("a + c = " + (a + c));
        System.out.println("a - c = " + (a - c));
        System.out.println("a * c = " + (a * c));
        System.out.println("a / c = " + (a / c));
        System.out.println("a % c = " + (a % c));
    }
}
